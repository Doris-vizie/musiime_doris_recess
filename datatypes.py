# Integer
y = 10
print("\nInteger example:", y)
print("Type:", type(y))

# Float
z = 3.14
print("\nFloat example:", z)
print("Type:", type(z))

# String
classname = "computing class"
print("\nString example:", classname)
print("Type:", type(classname))

# Boolean
isnot_valid = False
print("\nBoolean example:", isnot_valid)
print("Type:", type(isnot_valid))

# List
numbers = [10, 20, 30, 40, 50]
print("\nList example:", numbers)
print("Type:", type(numbers))

# Tuple
tup = (60, 80)
print("\nTuple example:", tup)
print("Type:", type(tup))

# Dictionary
registration = {"name": "Doris", "age": 20, "yard": "b1"}
print("\nDictionary example:", registration)
print("Type:", type(registration))
